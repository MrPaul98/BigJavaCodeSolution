public class Item
{
	private String key;
	private String value;
	
	public Item(String aKey, String aValue)
	{
		key = aKey;
		value = aValue;
	}
	
	public String getKey()
	{
		return key;
	}
	
	public String getValue()
	{
		return value;
	}
}